#include<stdio.h>
int main()
{
	int x=10,y=20;
	int sum,diff,prod,div;
	sum=x+y;
	diff=x-y;
	prod=x*y;
	div=x/y;
	
	printf("X value is: %d\n",x);
	printf("Y value is: %d\n",y);
	printf("Sum = %d\n",sum);
	printf("difference = %d\n",diff);
	printf("multiplication = %d\n",prod);
	printf("division = %d\n",div);
	return 0;
}
